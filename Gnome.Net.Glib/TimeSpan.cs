namespace Gnome.Net.Glib;

/// <summary>A static class with time spans information.</summary>
public static class TimeSpan
{
    /// <summary>Microseconds in a day.</summary>
    public const long Day = 86400000000;

    /// <summary>Microseconds in an hour.</summary>
    public const long Hour = 3600000000;

    /// <summary>Microseconds in a minute.</summary>
    public const long Minute = 60000000;

    /// <summary>Microseconds in a second.</summary>
    public const long Second = 1000000;

    /// <summary>Microseconds in a millisecond.</summary>
    public const long Millisecond = 1000;
}